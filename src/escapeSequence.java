public class escapeSequence {
    public static void main(String[] args) {
        System.out.println("I am \" Nimisha \"");
        // you have to use back-slash before double quotes
        System.out.println("It's my repository \\");
        // you have to use double back-slash for writing back-slash
        System.out.println("For new line \n");
        // you can write a new line by using \n
        System.out.println("for 4 spaces\t");
        //for a tab space use backslash t

    }
}
